#include "libft.h"

int	ft_toupper(int c)
{
	size_t	new_c;

	new_c = c;
	if (new_c >= 97 || c <= 122)
	{
		new_c = new_c - 32;
	}
	else
	{
		return (0);
	}
	return (new_c);
}
