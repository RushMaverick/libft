#include <unistd.h>
#include <stdio.h>
#include <string.h>

int	ft_strlen(const	char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	printf("%d", ft_strlen("Crocodile hunting is a hobby for the rich."));
	printf("%lu", strlen("Crocodile hunting is a hobby for the rich."));
}
